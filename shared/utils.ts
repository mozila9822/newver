export function generateSlug(title: string): string {
  return title
    .toLowerCase()
    .trim()
    .replace(/[^\w\s-]/g, '')
    .replace(/[\s_-]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

export function matchSlug(itemTitle: string, slug: string): boolean {
  return generateSlug(itemTitle) === slug;
}
